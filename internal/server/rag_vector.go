package server

import (
	"encoding/binary"
	"math"
)

// encodeVector/decodeVector store embeddings as a compact BLOB (4 bytes
// per float32, little-endian) rather than JSON text, since chunks tables
// can grow large and every query scans every row's vector.
func encodeVector(v []float32) []byte {
	buf := make([]byte, len(v)*4)
	for i, f := range v {
		binary.LittleEndian.PutUint32(buf[i*4:], math.Float32bits(f))
	}
	return buf
}

func decodeVector(b []byte) []float32 {
	n := len(b) / 4
	v := make([]float32, n)
	for i := 0; i < n; i++ {
		v[i] = math.Float32frombits(binary.LittleEndian.Uint32(b[i*4:]))
	}
	return v
}

// cosineSimilarity is computed in Go rather than via sqlite-vec, per the
// decision to keep the pure-Go cross-compile story intact (see ROADMAP.md
// Month 3 notes). Brute-force scanning is fine at the scale onebox
// targets; a real ANN index can replace this later without changing the
// API.
func cosineSimilarity(a, b []float32) float64 {
	if len(a) != len(b) || len(a) == 0 {
		return 0
	}
	var dot, normA, normB float64
	for i := range a {
		dot += float64(a[i]) * float64(b[i])
		normA += float64(a[i]) * float64(a[i])
		normB += float64(b[i]) * float64(b[i])
	}
	if normA == 0 || normB == 0 {
		return 0
	}
	return dot / (math.Sqrt(normA) * math.Sqrt(normB))
}
